package com.hi;

public class Ex10 {

	public static void main(String[] args) {
		System.out.println(Math.abs(12));	// abs 양수든 음수든 양수로 절대값을 리턴해준다
		System.out.println(Math.abs(-12));
		
		double su=3.14;
		System.out.println(Math.ceil(su));	// 올림
		System.out.println(Math.floor(su));	// 버림
		System.out.println(Math.round(su));	// 반올림
		
		System.out.println(Math.random());	// 랜덤
		
	}

}
