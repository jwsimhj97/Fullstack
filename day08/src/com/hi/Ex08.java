public class Ex08 {

	public static void main(String[] args) {
		int[] arr1={1,3,5,7};
		for(int i=0; i<arr1.length; i++){
			System.out.print(arr1[i]+",");
		}
		System.out.println();
		
		arr1=new int[]{2,4,6,8};
		for(int i=0; i<arr1.length; i++){
			System.out.print(arr1[i]+",");
		}
		System.out.println();
		
		arr1=new int[]{11,22,33,44,55,66};
		for(int i=0; i<arr1.length; i++){
			System.out.print(arr1[i]+",");
		}
		System.out.println();
	}

}
